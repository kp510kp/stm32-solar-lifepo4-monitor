#include "ili9488.h"
#include <string.h>

extern SPI_HandleTypeDef hspi1;

#define TFT_CS_PORT GPIOB
#define TFT_CS_PIN  GPIO_PIN_6
#define TFT_DC_PORT GPIOA
#define TFT_DC_PIN  GPIO_PIN_9
#define TFT_RST_PORT GPIOC
#define TFT_RST_PIN  GPIO_PIN_7
#define SD_CS_PORT GPIOA
#define SD_CS_PIN  GPIO_PIN_10

static void CS_LOW(void){ HAL_GPIO_WritePin(TFT_CS_PORT,TFT_CS_PIN,GPIO_PIN_RESET); }
static void CS_HIGH(void){ HAL_GPIO_WritePin(TFT_CS_PORT,TFT_CS_PIN,GPIO_PIN_SET); }
static void DC_CMD(void){ HAL_GPIO_WritePin(TFT_DC_PORT,TFT_DC_PIN,GPIO_PIN_RESET); }
static void DC_DATA(void){ HAL_GPIO_WritePin(TFT_DC_PORT,TFT_DC_PIN,GPIO_PIN_SET); }

static void WriteCmd(uint8_t cmd)
{
    HAL_GPIO_WritePin(SD_CS_PORT, SD_CS_PIN, GPIO_PIN_SET);
    DC_CMD(); CS_LOW();
    HAL_SPI_Transmit(&hspi1, &cmd, 1, HAL_MAX_DELAY);
    CS_HIGH();
}

static void WriteData(uint8_t data)
{
    DC_DATA(); CS_LOW();
    HAL_SPI_Transmit(&hspi1, &data, 1, HAL_MAX_DELAY);
    CS_HIGH();
}

static void SetAddr(uint16_t x0,uint16_t y0,uint16_t x1,uint16_t y1)
{
    WriteCmd(0x2A);
    WriteData(x0>>8); WriteData(x0);
    WriteData(x1>>8); WriteData(x1);

    WriteCmd(0x2B);
    WriteData(y0>>8); WriteData(y0);
    WriteData(y1>>8); WriteData(y1);

    WriteCmd(0x2C);
}

void ILI9488_Init(void)
{
    HAL_GPIO_WritePin(SD_CS_PORT, SD_CS_PIN, GPIO_PIN_SET);
    HAL_GPIO_WritePin(TFT_CS_PORT, TFT_CS_PIN, GPIO_PIN_SET);

    HAL_GPIO_WritePin(TFT_RST_PORT, TFT_RST_PIN, GPIO_PIN_RESET);
    HAL_Delay(100);
    HAL_GPIO_WritePin(TFT_RST_PORT, TFT_RST_PIN, GPIO_PIN_SET);
    HAL_Delay(200);

    WriteCmd(0x01);
    HAL_Delay(150);

    WriteCmd(0x28);

    WriteCmd(0xC0);
    WriteData(0x17);
    WriteData(0x15);

    WriteCmd(0xC1);
    WriteData(0x41);

    WriteCmd(0xC5);
    WriteData(0x00);
    WriteData(0x12);
    WriteData(0x80);

    WriteCmd(0x36);
    WriteData(0x48);

    WriteCmd(0x3A);
    WriteData(0x66);

    WriteCmd(0x11);
    HAL_Delay(120);

    WriteCmd(0x29);
    HAL_Delay(20);
}
void ILI9488_FillRect(uint16_t x,uint16_t y,uint16_t w,uint16_t h,uint16_t color)
{
    SetAddr(x, y, x + w - 1, y + h - 1);

    uint8_t r = (color & 0xF800) >> 8;
    uint8_t g = (color & 0x07E0) >> 3;
    uint8_t b = (color & 0x001F) << 3;

    DC_DATA();
    CS_LOW();

    for(uint32_t i = 0; i < (uint32_t)w * h; i++)
    {
        uint8_t data[3] = {r, g, b};
        HAL_SPI_Transmit(&hspi1, data, 3, HAL_MAX_DELAY);
    }

    CS_HIGH();
}
void ILI9488_FillScreen(uint16_t color)
{
    ILI9488_FillRect(0,0,320,480,color);
}

