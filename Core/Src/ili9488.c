static void ILI9488_DrawPixel(uint16_t x, uint16_t y, uint16_t color)
{
    ILI9488_FillRect(x, y, 1, 1, color);
}

static void ILI9488_DrawChar(uint16_t x, uint16_t y, char c,
                             uint16_t color, uint16_t bg, uint8_t size)
{

    // If unsupported character, show blank
    if (c < 0 || c > 127)
    {
        c = ' ';
    }

    for (uint8_t col = 0; col < 5; col++)
    {
        uint8_t line = font5x7[(uint8_t)c][col];

        for (uint8_t row = 0; row < 8; row++)
        {
            uint16_t drawColor = (line & 0x01) ? color : bg;

            if (size == 1)
            {
                ILI9488_DrawPixel(x + col, y + row, drawColor);
            }
            else
            {
                ILI9488_FillRect(x + col * size,
                                 y + row * size,
                                 size,
                                 size,
                                 drawColor);
            }

            line >>= 1;
        }
    }

    ILI9488_FillRect(x + 5 * size, y, size, 8 * size, bg);
}

void ILI9488_Print(uint16_t x, uint16_t y, char *str,
                   uint16_t color, uint16_t bg, uint8_t size)
{
    while (*str)
    {
        ILI9488_DrawChar(x, y, *str, color, bg, size);
        x += 6 * size;
        str++;
    }
}
